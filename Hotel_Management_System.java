    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        
        char[] password = {'a', 'd', 'm', 'i', 'n', '1', '2', '3'};
        char [] inputPassword = jPasswordField1.getPassword();
        
        if (txtUsername.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please fill out username");
        }
        else if(jPasswordField1.getPassword().equals("")){
            JOptionPane.showMessageDialog(null, "Please fill out password");
        }
        
        else if (txtUsername.getText().contains("admin")&& Arrays.equals(inputPassword, password)){
            JOptionPane.showMessageDialog(null, "Login Succesful");
            CustomerCheckIn2 a = new CustomerCheckIn2();
            a.setVisible(true);
            this.setVisible(false);
        }
        
        else {
        JOptionPane.showMessageDialog(null, "Wrong username or password!", "Message", JOptionPane.ERROR_MESSAGE);}
    }//GEN-LAST:event_btnLoginActionPerformed

